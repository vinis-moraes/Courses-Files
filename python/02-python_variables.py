# Assign a value to variable
x = 5
y = "John"
# Print variables
print(x)
print(y)
# You can change the type of the variable after declaring it
z = 3
print("z = {}" .format(z))
z = "Sally"
print("now, z = {}".format(z))
# Casting
a = str("3")
b = int(3)
c = float(3)
print("{}, {}, {:.2f}".format(a, b, c))
# Types of variables
print(type(x))
print(type(y))
